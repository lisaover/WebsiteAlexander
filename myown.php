<p class="bold">Booklist Copyright 1997, American Library Association</p>

<p>&ldquo;Alexander continues her autobiography, which she began in <span class="italic">Taking Hold: My Journey into Blindness</span> (1994).  Blind and just starting out on her own in an apartment in Pittsburgh, she must find her way to the bus stop, grocer, cleaners, etc., and learn how to deal with the relationships and anxieties of her new job teaching life skills to the newly blind. As the story continues, Alexander attempts to resume a social life and starts graduate school. She is devastated when the last bits of her vision disappear and she suffers some hearing loss. Readers will follow her troubles and her rebounding spirit with heartache and hope.  [This book] is as uplifting as the first book and is sure to be read with eagerness.&rdquo;</p>

