  <h2>Sensitivity Readers</h2>
  
  <img src="images/Dave-and-Carl-1.jpg" alt="Sally by a pool with Dave and granddog Carl, who is licking her face." class="floatLeft" />
  
 <p>Even though blind Suzie lived in a world of darkness, she had a sunny disposition!</p>
  
<p>That sentiment would get the &ldquo;Oh gag award&rdquo; in any book, about any character, which 
I can bestow, since I was a recipient&mdash;once&mdash;for a particularly saccharine phrase. 
But that sentence is not only a sappy clich&#0233;, it&rsquo;s a complete mistake. Blindness isn&rsquo;t darkness!  
It isn&rsquo;t midnight.  It&rsquo;s nothingness which looks like mist&mdash;all the time, day or night.
Yet, that sentence has appeared almost verbatim in many award-winning books
by award-winning authors&mdash;all of them sighted, even in the ALA 2017 best
books.</p>

<p>I didn&rsquo;t realize how angry such errors made me until I nearly tossed my
defenseless audio book machine out the window, in response.  Unless more
care is taken by publishers for accuracy in portraying the disability
experience, I&rsquo;ll have to read unbreakable Braille books.</p>

<p>Seriously, I support anyone writing about any subject, about any character
he/she wants to write about.  I do not think only blind people can write
about blindness.  But sighted authors have to get the facts straight.
Years of research wouldn&rsquo;t have been necessary for the author of the flawed
book (that turned me violent) to learn that counting steps is a blind
person&rsquo;s last-resort tool for finding destination, that touching the face
shows what the person feels, not looks like.  The most minimal fact-finding
approach, simply interviewing a sampling of congenitally and adventitiously
disabled people, would reduce the bulk of the stereotypes.  With the
cultural appropriation concerns today, I&rsquo;m pretty astonished that agents and
editors aren&rsquo;t more attentive to possible errors by able-bodied authors in
books about disability.</p>

<p>So, in response, I&rsquo;m casting myself as a sensitivity reader, particularly on
blindness, deafness, and Deaf-Blindness.  Please contact me for help, and
in addition, I&rsquo;ll offer Dave and our grand dog Carl&rsquo;s assistance in sniffing
out all stinky grammar and unleashed doggerel.</p>
  
