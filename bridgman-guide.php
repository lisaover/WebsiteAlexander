  <p>My newest book, co-authored with my husband, Bob Alexander, is a biography of the first successfully-educated Deaf-Blind person, fifty years before Helen Keller.  Without sight, hearing, smell, or taste, Laura became the blueprint for future generations of the Deaf-Blind and all disabled people.</p>
  <p>We are thrilled that the book has been chosen as a Junior Library Guild selection.   In a starred review, Kirkus called it &ldquo;a valuable and long-overdue addition to library shelves.&rdquo;  In another starred review, School Library Journal said, &ldquo;The authors of this meticulously researched biography convey Bridgman&rsquo;s world of touch and sensation in terms children will understand: &lsquo;The sun was heat on her face.&rsquo; Mountains were sloped, uneven paths to climb.&rsquo;&rdquo;</p>
  <p>In classrooms, training centers, and libraries, <span class="italic">She Touched the World</span> can be used to:</p>
  <ul>
  	<li>learn about one person&rsquo;s remarkable life</li>
	<li>show how touch and any remaining sense can be trained to substitute for lost ones </li>
	<li>practice the manual alphabet of the deaf</li>
	<li>discuss new laws, medicines, attitudes, and technology  which enhance disabled people&rsquo;s independence </li>
	<li>inspire readers to overcome obstacles</li>
	<li>study important conflicts and culture in  nineteenth century New England </li>
	<li>learn about other significant nineteenth century figures, such as Charles Dickens, Charles Darwin, and Dorothea Dix</li>
	<li>discuss the term &ldquo;pioneer,&rdquo; and challenges of other pioneers</li>
	<li>discuss research techniques, including use of written sources and period images</li>
	<li>illustrate good nonfiction story-telling, using letters, journals, and reports, not fictional dialogue</li>
  </ul>
