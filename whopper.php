<h2>Reviews</h2>

<p class="bold">School Library Journal, 1992</p>

<p>&ldquo;Maggie is a seven-year-old spending time with her Great-uncle Ezra at a lake cottage. Her great hope is to catch a fish, a real whopper, to show her older brother. Early in the story, the girl and her uncle have a run-in with an old bear when it claws open a screen to get a blueberry pie. Later, when Ezra is caught off guard by the hungry animal, Maggie&rsquo;s quick thinking saves the day. The story is well plotted with foreshadowing of events and credible action. Where it does succeed is in depicting the affection between the child and the old man. They enjoy one another&rsquo;s company whether fishing, playing checkers, or taking a moonlit hike. Ray&rsquo;s sketchy, soft-focused watercolor and colored-pencil drawings add to this gentle and intimate tone. For a plausible scenario and a likable heroine, it&rsquo;s a winner.&rdquo;</p>

<p class="bold">Kirkus, 1992</p>

<p>&ldquo;Maggie, seven, who enjoys fishing with Great-uncle Ezra, hopes to outdo her older brother by catching a real &lsquo;whopper.&rsquo; Meanwhile, despite her uncle&rsquo;s reassurance, she worries about meeting old Thatch, a bear they&rsquo;ve sometimes sighted near their pond. Uncle Ezra suggests that the bear is an old man...like me&rsquo; and may even have died, but Thatch turns up in full vigor, breaking through a screen to steal a pie. Later, Maggie catches her whopper just as Thatch reappears, looking none too friendly. Uncle Ezra is visibly afraid, but Maggie manages to divert the bear by throwing him the fish. Several themes are introduced here, Uncle Ezra&rsquo;s frailty, coexistence with the bear, Maggie&rsquo;s rivalry with her offstage brother, Maggie gets her heroic moment. A warmhearted vignette, attractively reflected in Ray&rsquo;s glowing generalized illustrations.&rdquo; <!-- (Picture book. 4-8) --><p>
