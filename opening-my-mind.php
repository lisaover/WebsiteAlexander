  <h2>Opening My Mind</h2>
  
<p>by Sally Hobart Alexander</p>
  
<p>With National Library Week coming up, I&rsquo;m reminded of the day I discovered books. I lay between the crisp, chill sheets of the hospital bed with my eyes patched, trying to decode the Braille letters in the manual on my lap.
</p>
<p>I read the letter &ldquo;A&rdquo; easily&mdash;just one lonely bump. &ldquo;B,&rdquo; like a raised colon, and &ldquo;C,&rdquo; an embossed hyphen, were also straightforward. But even when I scraped with my fingernail, the rest of the letters bunched together like cold rice.
</p>
<p>My hand dropped to the mattress. I was pathetic at this exercise. Pathetic generally, having spent most of the previous 10 months in East or West Coast hospitals, having tests, laser treatments and eyes patched to allow retinal hemorrhages to settle.
</p>
<p>Not the life I&rsquo;d been living in my mid&ndash;20s&mdash;no third-grade students, no California beach walks, no sailing or tennis, and no diagnosis or sense of how long my limited sight would last.
</p>
<p>&ldquo;Sally?&rdquo;
</p>
<p>It was Celeste, my nurse. Rolling, squeaking wheels of a cart accompanied her footsteps. &ldquo;From our local library I got a talking-book machine for you.&rdquo;
</p>
<p>&ldquo;What&rsquo;s that?&rdquo; I asked.
</p>
<p>&ldquo;A record player that plays books.&rdquo; She placed my hand on the needle. She gave me earphones. &ldquo;What do you want to listen to, &rsquo;The Great Gatsby?&lsquo; &rsquo;Pride and Prejudice&lsquo;?&rdquo;
</p>
<p>Voila! I morphed into a book lover&mdash;probably the most important transformation of my life.
</p>
<p>Of course, I&rsquo;d read before that day in October 1968&mdash;every book assigned in high school and college. As a teacher, I&rsquo;d studied books about classroom management and child development. But I&rsquo;d never read for fun.
</p>
<p>I was a swimmer, a beach volleyball player, a water ballet nut. Reading had always seemed too quiet.
</p>
<p>But trapped in bed with just one object in mind&mdash;to let the retinal blood settle&mdash;listening to books beat worrying, and it sure topped deciphering Braille.
</p>
<p>During that three-week stay, I &ldquo;read&rdquo; fiction. Back home, I read inspirational memoirs and other nonfiction. Through my rehabilitation training, I read medieval classics like &ldquo;Beowulf.&rdquo;
</p>
<p>During my first job after I became blind, I took a Shakespeare class. I went to graduate school in social work to change careers and read Haim Ginott and other child psychiatrists. I married an English professor. If I hadn&rsquo;t fallen in love with books, I wouldn&rsquo;t have fallen for my husband, or he, for me.
</p>
<p>Two years later we had a son, and after that, a daughter&mdash;both sighted. Since there weren&rsquo;t many Braille books with print and pictures, I curled up with the kids around a tape player or listened while my husband read to them. I devoured books I&rsquo;d never read, like &ldquo;Wind in the Willows.&rdquo; And on walks with our daughter in a backpack, our son in a child&rsquo;s harness and leash, and my guide dog leading the party, I told them stories.
</p>
<p>Soon I heard about a writing workshop in a local children&rsquo;s book store. &ldquo;I think I&rsquo;ll join a critique group,&rdquo; I told my husband.
</p>
<p>Now eight published books later, I&rsquo;m hooked. Not only do I read daily, I write constantly. I gave up the social work career to write, but I use my social work skills when I lead critique groups out of my home and teach Chatham M.F.A. students. I could never have become a writer without first becoming a reader.
</p>
<p>Discovering books not only enriched my life and led me to my husband, family and a new career, it clarified my thinking, my values, my faith.
</p>
<p>Reading had another unplanned consequence. It addicted me to learning and gave me a mental life. As a former jock, I still find ways to enjoy physical pursuits&mdash;I hike with my guide dog, swim, work out on a tread-mill, tandem bike and kayak. Nevertheless, finding enjoyment in things of the mind is an invaluable byproduct of reading.
</p>
<p>Since over the years, I&rsquo;ve also developed serious hearing problems, I take solace in knowing that Braille books will always be available from the library. Although I don&rsquo;t minimize the challenges of my deaf-blindness, I do believe that were I to lose all my hearing, I would still find meaning and joy in reading and writing books.
</p>
<p>Finally, reviewers of my books often conclude with some variation of the line from &ldquo;Amazing Grace&rdquo;: &ldquo;Was blind, but now she sees.&rdquo;
</p>
<p>As a writer, I rebel against that cliche. But when books came into my hospital room and into my life as I went blind, they may not have opened my eyes or my ears, but they opened my mind. And for that, I thank the libraries across America.
</p>
