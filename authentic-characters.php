  <h2>Writing Authentic Characters Who are Blind or Visually Impaired</h2>
  
<p>by Sally Hobart Alexander</p>
  
<p>I&rsquo;ve lived on both sides of disability, becoming blind at 26, losing substantial hearing at 28.  So, it&rsquo;s no accident that six of my eight books deal with sight or hearing loss.  Recently, I&rsquo;m again on both sides, this time of the Own Voices issue.  I find sighted authors getting it wrong&mdash;i.e., describing total blindness as &ldquo;darkness,&rdquo; not mist&mdash;yet I want to write a biography of a blind man who was African-American, but I’m White.  </p>
<p>This dual experience makes me seek more input from African-
American critics, but also prompts me to offer help to sighted people writing about visual impairment.  </p>
<p class="bold">Information for developing Blind characters and their challenges:</p>
<ol>
<li>Some of us are totally blind, but most have some sight. Legal blindness means that the person sees at 20 feet with the best correction what sighted folks see at 200&mdash;pretty useful sight. </li>
<li>Someone who is born blind or blinded in youth will experience the feeling of difference from others and may be teased or isolated.  She will train her hearing automatically (echo location) and may consider blindness an attribute like hair color.  Someone blinded in adulthood will experience the sensation of difference, but also the grief from the loss.  Most will heal and benefit from their visual memories, but will probably consider blindness a deprivation. </li>
<li>Only about 10% of blind people know Braille.  Most without sight use canes.  Approximately 2% use guide dogs, and to qualify for a guide, one must be at least eighteen.</li>
<li>Functioning independently takes more time.  To match our clothing, we pin Braille color tags to labels.  We need help in learning new routes to locations. We scramble to keep up with changing technology.  We must keep our homes uncluttered, so living with sighted folks is tricky&mdash;when my husband leaves his chair out from the table and my shins find it, I accuse him of passive aggression. </li>
<li>Still, functioning in our homes is much easier than functioning outside.  Inside, we seldom use canes or dogs, just feel surfaces underfoot, hardwood, tile, carpeting.  We trail our fingers along walls and furniture, occasionally encountering a distracted guest&rsquo;s genitalia. </li>
<li>Outside we rely more on our hearing than touch, though rough sidewalks or bumps are useful in orientation.  Though cars are dangers, they are also friends.  We parallel them to walk straight down a sidewalk; the traffic flow pinpoints the elusive opposite curb and tells us if a light is green or red.  Dogs do not see these colors.  </li>
<li>Outside, we face ice and snow, &ldquo;blind person’s fog&rdquo; and noises that block the vehicular sounds: pneumatic drills, sirens, helicopters, barking dogs. </li>
<li>We meet garbage cans, cars, fallen branches, or construction on sidewalks.</li>
</ol>

<p class="bold">Occasional goofs by the sighted public:</p>
<ol>
<li>Shouting when speaking to us, as if we are deaf, </li>
<li>Addressing our companions who can see, because we can&rsquo;t make eye contact: &ldquo;What does she want?&rdquo; </li>
<li>Moving away  without telling us, so we speak to empty chairs, </li>
<li>Using phrases requiring a gesture, like &ldquo;over there,&rdquo; </li>
<li>Using nonverbal communication, waves, nods, eye rolls, </li>
<li>Or phrases of disability as metaphors, &ldquo;blind to his faults,&rdquo; </li>
<li>Trying to help a blind person and pulling him before asking if he needs assistance. </li>
  </ol>
<p class="bold">Literary stereotypes:</p>
<ol>
<li>Along with the darkness metaphor, there&rsquo;s the counting steps error.  It&rsquo;s imprecise and a last resort technique for finding locations. </li>
<li>Touching faces to &ldquo;see&rdquo; what people look like:  Noooo!  Touch is intimate. Besides, many blind people have no concept of vision, so appearance isn&rsquo;t a priority.</li>
<li>Guide dogs aren&rsquo;t cab drivers.  The dog and human are a team&mdash;the dog is pilot, and the blind person the navigator.</li>
</ol>

<p>Most sighted people are savvy.   Yet, mistakes in books with blind characters continue, even by well-respected, award-winning authors.  As with anything we haven&srquo;t experienced, we writers turn to our best tool&mdash;research.  </p>

