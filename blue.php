<h2>Reviews</h2>

<p class="bold">School Library Journal, 2000</p>

<p>&ldquo;As she has done in past books, Alexander makes blindness clear to readers. Here, she responds to frequently asked questions, including how it feels to be blind and how blind people cope with daily living. She tells how she lost her sight and recounts the fears she experienced at the time. Readers learn how she gets around and how she writes. The author briefly describes the tools she uses, from talking books to machines that transpose the written word into vibrations to be read with the fingers. She spends some time discussing problems related to dating, how she met her husband, and how she was able to raise two children. Her discussion of remembering colors and dimensions will interest sighted readers. Small black-and-white photos appear throughout; most are snapshots from the personal collections of the author and her friends. A list of organizations for additional information is included.&rdquo;</p>

<p class="bold">Kirkus Associates, 2000</p>

<p>&ldquo;An eloquent memoir in questions and answers, as Alexander (<span class="italic">On My Own</span>, 1997, etc.) tackles the children&rsquo;s queries she most often encounters. The essay format is a departure for Alexander.  Her story of blindness began in the autobiography <span class="italic">Taking Hold</span> (1995) but it retains the anecdotal intimacy of the previous book. Ultimately upbeat, she conversationally recounts her journey from fear and despair to having a family and becoming a successful writer. Without self-pity, she describes the difficulties and frustrations of learning how to negotiate a world designed for the sighted. Her sense of humor especially when talking about her family, or mistakes she has made in the process of living, is always apparent, but it gains strength in her honesty. Her answers are followed by seven tasks that palpably simulate blindness for readers; there is also a short list of helpful tools, e.g., Braille clothing tags that identify the color of the cloth, along with a discussion of ways the blind benefit from modern technology. A witty, wise, inspiring book.&rdquo; <!-- (Nonfiction. 9-12) --><p>

