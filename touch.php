<p>In the new movie <span class="italic">Atonement</span>, Celia holds her lover&rsquo;s hand under her parents&rsquo; dining room table and again in a restaurant before he leaves for war.  This uncomplicated act communicates the depth of feeling between them, despite the enormous complications of their relationship.  And again in the new release, <span class="italic">The Kite Runner</span>, all that is positive between the main character Amir and his best friend Hassan is communicated through simple nonverbal gestures, linking arms and hugging.  </p>
<p>In movies and in real life, we convey complex messages through touch endlessly, yet unconsciously.  As a person who is totally blind and partially deaf, I am a devotee of the sense of touch.  When I lost my sight at twenty-six and some hearing at twenty-eight, I learned to distinguish between minute patterns of bumps to read Braille.  With a cane acting as an extension to my arm, I felt the ups, downs, and textures of sidewalks and the objects that might trip me.  Touch seared facts into my memory.  When my guide dog led me into a sign post, causing my nose to bleed, I didn&rsquo;t forget the location of the offending post, and neither did she.  To function in a new place, I moved from wall to wall, feeling every piece of furniture; to memorize phone numbers or addresses, I felt them in Braille. My hands and fingers became tentacles.</p>

<img src="images/touch/dolphins2.jpg" alt="This is the second of four thumbnail photos of me swimming with and stroking a dolphin" class="floatLeft" />	

<p>In those difficult days of loss and new challenge, I reaped the benefits of the reassuring power of touch.  People&rsquo;s hugs, caresses, and strokes all transmitted support, affection, and sympathy.</p> 
<p>Because these tactile communications are so diverse, yet so subtle and familiar, I didn&rsquo;t realize their power.  Like most people, I&rsquo;d grown up considering touch a pitiful step-sister or step-brother to the more dominant, valued older sibs, sight and hearing.  The pedophilia scandals of recent years have further diminished the perceived value of the sense.</p>

<img src="images/touch/dolphins4.jpg" alt="This is the third of four thumbnail photos of me swimming with and stroking a dolphin" class="floatRight" />

<p>But my disabilities made me conscious of the power of touch.  Fingertips are astonishing sense organs.  Feet are even better and, frankly, the tongue best of all.  And they are just parts of the largest sense organ, skin.  The cloak over my whole being, skin connects me to others and to the environment. With the skin&rsquo;s infinite number of nerve endings, I touch and am touched.</p>
<p>Most people enjoy the erotic aspects of this sense, kissing, holding, caressing, and having sex.  When my children were born, I learned that parenting provided a new tactile pleasure.  I enjoyed my babies&rsquo; kicking in utero.  When they came into the world, I nursed them for over a year, playing tummy games all the while.  Since baby carriages and strollers were difficult to manage, I carried them in front packs and backpacks or held them in one arm and walked for miles.  I parented them in the manner of a mother chimp, intertwined, our hearts beating in unison.</p>
<p>A down side to employing this sense when blind is accidentally poking people&rsquo;s noses, or worse, their genitalia.  This is more &lsquo;touchy-feely&rsquo; than I strive to be!</p>

<img src="images/touch/dolphins3.jpg" alt="This is the fourth of four thumbnail photos of me swimming with and stroking a dolphin" class="floatLeft" />

<p>Still, my experience with touch has mostly up sides.  I walk for miles with my guide dog, feeling her wiggle through the harness.  I rest my head on my husband&rsquo;s shoulder, while he describes the hand-holding and other silent parts of movies.  I ride a tandem bike, bob in a tandem kayak, swim through warm and cold spots of a lake with a rope around my wrist attached to the boat my husband rows.  I hike and dance and relish the spring wind, summer rain, winter snow.  I live my life with antenna-skin, always keeping in touch.</p>

