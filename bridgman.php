  <p>Jacket Copy: <span class="italic">&ldquo;Fifty years before Helen Keller, there was Laura Bridgman&rdquo;</span></p>

  <p>&ldquo;At the age of twelve, Laura Bridgman was world famous. She performed onstage and met other celebrities of her time. Politicians, artists, and reformers flocked to see her, and she was loved and admired by children everywhere. </p>

<p>Laura Bridgman was also deaf and blind. And her journey to fame wasn&rsquo;t easy. At first, no one believed that a child like her, with such severe disabilities, could even be reached. While other kids her age played and went to school, Laura sat idle, trapped in the prison of her own body. Even her parents feared she was hopeless. </p>

<p>But Laura was bright and curious, and she yearned to connect with the world around her. Finally, with the help of a remarkable doctor, she learned to communicate, read, and write, and eventually even to teach. Laura proved to everyone that people like her could be educated. They weren&rsquo;t hopeless, after all.</p>
 
<p>As a blind person with some hearing loss, coauthor Sally Alexander lends a unique perspective to this inspiring biography, showing how one little girl paved the way for future generations of the Deaf-blind, including Helen Keller. At last, we know the story of Laura Bridgman; of how, against all odds, she reached out from her realm of clouds and silence, and touched the world.&rdquo; </p>


