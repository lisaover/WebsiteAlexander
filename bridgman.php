<h2>About the Authors</h2>

<p>Here is what our publisher, Clarion, says about us:</p>

<p>&ldquo;Sally Hobart Alexander teaches literature and writing in the MFA program at Chatham University and is well known for her books about her experiences as a blind person. When Sally lost her sight at the age of twenty-six, she heard about the remarkable accomplishments of Laura Bridgman. As she began to have hearing problems a year later, Laura&rsquo;s story of challenge and triumph took on an even deeper meaning for her. She felt a growing kinship with Laura, which led her to write this book. </p>

<p>Robert Alexander is a professor of English and director of the writing program at Point Park University. He studies early English theater history. This is the first book the Alexanders have written together. They have two adult children and live in Pittsburgh, Pennsylvania.&rdquo;</p>

<h2>Reviews</h2>

<p class="bold">Kirkus</p>
<p>Starred Review</p>

<p>&ldquo;The first full-length new biography of Bridgman for young readers since Edith F. Hunter&rsquo;s <span class="italic">Child of the Silent Night</span> (1963) offers a salutary reminder that Helen Keller wasn&rsquo;t the only, or even first, woman to prove that deafness and blindness are not unsurpassable obstacles to becoming a functional member of society. Though a still-undiagnosed childhood disease left her with only her hands for a sense organ and &lsquo;an endless curiosity,&rsquo; Bridgman responded so well to the efforts of early educator Samuel Gridley Howe, head of the first school for blind children in America, that she became an international celebrity in the 1840s. This provided evidence for the startling new idea that disabled, even multiply disabled, people could be intelligent, educable and productive. The authors (one of whom is blind and partially deaf herself) cap their profile with a long afterward analyzing the changes of attitude that Bridgman helped to spark, and describing modern support systems for disabled people. Illustrated with period photos and prints, and supported by extensive notes and resource lists, this will be a valuable and long-overdue addition to library shelves.&rdquo;  <!-- (<span class="italic">Biography. 10-12</span>) --></p>

<p class="bold">Booklist</p>

<p>Advanced Review - Uncorrected Proof, Issue: March 1, 2008</p>

<p>&ldquo;At the age of three, in 1832, Laura Bridgman contracted scarlet fever and lost her sight, her hearing, her sense of smell, and much of her sense of taste. Her family sent her to Dr. Samuel Gridley Howe at the New England Institute for the Education of the Blind, and by the age of 10, Laura was world-famous for her accomplishments (with admirers ranging from Charles Dickens to Dorothea Dix), and also a success story for Howe&rsquo;s teaching methods. Alexander, known for books about her own experiences as a blind person, presents a well-written and thoroughly researched biography of this remarkable woman, with numerous black-and-white photos (quality was hard to determine in the galley). There&rsquo;s little available on Bridgman for young readers, so this will be a welcome addition to many collections. An appended listing of Web sites and books will lead readers on to more.&rdquo;</p>
<p class="quote">&mdash;Shauna Yusk</p>

<p class="bold">School Library Journal</p>
<p>Starred Review</p>

<p>&ldquo;In the early 1840s, Bridgman was known throughout the world for her educational accomplishments despite her disabilities. Yet she would be so overshadowed by Helen Keller 50 years later that it is now impossible to mention her without drawing comparisons to Keller. In fact, Bridgman&rsquo;s education, undertaken by Dr. Samuel Gridley Howe of the New England Institution for the Education of the Blind, laid the foundation for Keller&rsquo;s accomplishments (Bridgman taught Annie Sullivan how to fingerspell), and for the education of Deaf-Blind children even today. The authors of this meticulously researched biography convey Bridgman&rsquo;s world of touch and sensation in terms children will understand: &lsquo;The sun was heat on her face.&rsquo; Mountains were sloped, uneven paths to climb. Details such as the child&rsquo;s daily school schedule allow readers to connect her story with their own lives. Photos and illustrations of unfamiliar historical objects give context throughout, as does the authors&rsquo; explanation of period medical studies such as phrenology. Only one detail causes concern: In a caption about the debate over whether to use sign language with children, the authors correctly note that it was &lsquo;denounced as crude pantomime,&rsquo; yet fail to mention that American Sign Language has since been proven to contain all of the grammar and linguistic structures that spoken languages have. The afterward, &ldquo;If Laura Were Alive Today,&rdquo; describes the medical and technological advances that affect Deaf-Blind individuals today by introducing Deaf-Blind coauthor Sally Hobart Alexander.&rdquo;</p>
<p class="quote">&mdash;Kathleen Kelly MacMillan, Carroll County Public Library, MD</p>
