  <h4>Pre-reading Activities</h4>
  <ol>
  	<li>Do several of the Tricks from the Training Program.</li>
	<li>Observe a few of the Useful Tools, described in the book.</li>
	<li>Try to identify objects by touch only, such as coins.  No peeking.</li>
	<li>Close your eyes, and see if you can tell who is saying hello to you.  Then, close your eyes again, and have a friend place a book in front, in back, or beside your head and try to determine where it is, simply through your hearing.  </li>
	</ol>
  
<h4>Thematic Issues</h4>
<ol>
	<li>Difference: People do things differently.  Blind people use their hearing and touch to accomplish the tasks you accomplish by seeing.  People throughout the world use different implements for eating and eat different foods.  Yet, sometimes we are afraid of different people.  Why?</li>
	<li>Overcoming Difficulties: Discuss challenges faced by people you know or by famous people.  How did they overcome them?  What factors combined to allow them to cope?</li>
	<li>Stereotypes and Illusions: many people attribute qualities to blind people that may or may not fit.  What stereotypes do you think blind people suffer from?  What do you think of using terms of disability as metaphor, i.e., blind to his faults, deaf to her wishes?  Many people think that dog guides make blind people perfectly independent.  They are an extraordinary help, but they are not the perfect substitute for sight, any more than hearing aids are the perfect restorer of hearing.  What other illusions do you have about disabled people?  About other people who are different?</li>
	<li>Technology: Progress in technology has made so much more available to disabled people.  Discuss.   </li>
</ol>

<h4>Interdisciplinary Activities</h4>
<ol>
	<li>Make collages in art class, using tactual and fragrant materials.</li>
	<li>Use the Braille alphabet in reading class and try to reconstruct your name in dots with a pencil, corresponding to the Braille raised dots.</li>
	<li>Learn how to write Braille numbers in math class.</li>
	<li>In social studies talk about fifteen-year-old Louis Braille, about Laura Bridgman, first successfully educated deaf-blind person, and about Helen Keller.</li>
	<li>Have a science lesson on senses.  A dog&rsquo;s sense of smell is its keenest sense, even better than hearing.  Most smaller animals use smell and taste as effectively as we humans use hearing and sight. </li>
</ol>

