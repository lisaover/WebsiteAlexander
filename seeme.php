<p class="bold">Booklist, Editors&rsquo; Choice</p>

<p>&ldquo;&#8230;a refreshingly honest and open story of a young girl who loves her mother.  Sometimes she feels embarrassed when her mother acts differently from other mothers.  Other times she feels sad that her mother can&rsquo;t see her or her brother.  Usually, though, she&rsquo;s aware that a lack of sight has not diminished her mother&rsquo;s ability to take a more active role  and derive more pleasure from life than many sighted people.  Many stories about people with disabilities leave questions unanswered and feelings unaddressed.  Not this one.  Approaching the issue with an attitude that rewards honesty, encourages curiosity,  and embraces open communication, <span class="italic">Mom Can&rsquo;t See Me</span> invites the reader to re-examine his or her own attitudes.  The story is candid and relaxed in tone, easily dispelling any fears of the unknown one may experience with disabled people.  This well-written photo-essay provides an excellent way for adults to engender a positive outlook in children and themselves regarding disability.&rdquo;</p>

