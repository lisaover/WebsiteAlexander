  <h2>Rescued: A Bus Driver Saved Me</h2>
  
<p>by Sally Hobart Alexander</p>
  
    <p>Recently I was reminded that my blindness puts me at risk and that my guide dog can goof.  I decided to meet my husband at the corner of Morewood and Forbes where he&rsquo;d arrive on the airport bus.  
	</p>
	<p>I&rsquo;d made the trek down Forbes Avenue many times, and after a week of living solo, I was high on independence. 
	</p>
	<p>When I set out with Flossie, the weather stank, raining heavily.  I heard no other pedestrian footsteps.  At the corner of Wightman and Forbes, I turned west.  Flossie whined, because any time I turned that way I walked to Craig Street or Morewood.  It&rsquo;s a 30&mdash;40 minute walk, not including the return trip, usually made at night, when she&rsquo;d rather be in her cozy crate.  Though daylight now, it poured.  I carried an umbrella that didn&rsquo;t shield her.  She shook and sprayed me while we waited, in revenge.
	</p>
	<p>&ldquo;Flossie, forward,&rdquo; I said when the first car moved on Forbes.  It headed east.  In hindsight, I should have waited to parallel a car moving west beside me.  
	</p>
	<p>Flossie obeyed, but just as I was beginning to judge that enough time had elapsed for her to have reached the opposite curb, I knew I was still in the street.  Yikes!  
	</p>
	<p>Okay.  Scenario 1&mdash;she veered into Forbes, so I should give right commands.  I listened for the cars.  None.  Scenario 2&mdash;she veered right into Wightman&mdash;
	</p>
	<p>&ldquo;Beep! Beeeep!&rdquo;  A car whizzed past on my left.  I knew from the sound it wasn&rsquo;t grinding up Wightman hill, so neither was I.  Forbes, I concluded.  
	</p>
	<p>&ldquo;Right,&rdquo; I screamed, &ldquo;Oh God, right, Flossie!&rdquo;
	</p>
	<p>Just as I stepped onto the sidewalk, someone grabbed my elbow.  &ldquo;You&rsquo;re in the street,&rdquo; a man called.  He jerked me forward.  
	</p>
	<p>I was grateful, but trying to process everything.  Looking back now, I realized&mdash;vision isn&rsquo;t just the dominant sense.  It&rsquo;s the most efficient.  Hearing and touch suffice as substitutes, but they&rsquo;re slower in processing information.  One glance, should I have been granted a temporary peek, would have told me exactly what had happened&mdash;what to do.  But using hearing and touch, I had to absorb, then analyze all the stimuli coming my way. 
	</p>
	<p>&ldquo;What were you doing?&rdquo; the man continued&mdash;his tone suggesting that I should be home where I belonged.  &ldquo;A car almost hit you!&rdquo;
	</p>
	<p>Shame.  My failings exposed.  Conflict&mdash;I felt grateful, but lousy, too.
	</p>
	<p>&ldquo;Where do you want to go?&rdquo;
	</p>
	<p>&ldquo;Murray,&rdquo; I said, but, of course, I meant Murdoch.  I wasn&rsquo;t thinking clearly.  Embarrassment did that to me.  
	</p>
	<p>The man pushed me in some direction across some street.  &ldquo;I have to go,&rdquo; he said.  &ldquo;I have a bus full of people.&rdquo;
	</p>
	<p>Oh no.  A bus driver.  He&rsquo;d pulled over to rescue me.  What a good, amazing guy!
	</p>
	<p>&ldquo;Okay, Murray is that way.&rdquo;
	</p>
	<p>&ldquo;I wanted Murdoch,&rdquo; I said.
	</p>
	<p>&ldquo;My passengers...I...&rdquo;
	</p>
	<p>&ldquo;What&rsquo;s the street on my right,&rdquo; I called as he rushed away.
	</p>
	<p>&ldquo;Wightman.&rdquo;
	</p>
	<p>&ldquo;And the street in front of me?&rdquo;
	</p>
	<p>&ldquo;Forbes.&rdquo; And then I heard him, &ldquo;Sorry everybody.&rdquo;
	</p>
	<p>Oh no.  How many people witnessed my complete incompetence?  What kind of representative of the blind was I?  I wanted to yell to the passengers, &ldquo;I never do this!  I&rsquo;m generally pretty adept, even graceful.  I&rsquo;ve just pulled off a week without my husband...&rdquo; But the bus roared off&mdash;behind schedule no doubt. 
	</p>
	<p>Then it hit me.  I was more worried about appearance than danger.  And what about the danger I could have caused&mdash;a car swerving away from me?  Sometime I&rsquo;d have to talk to a psychologist about the focus on the lesser of two problems.
	</p>
	<p>For now, I pulled a U&mdash;turn, crossing Wightman, Forbes, then Wightman again.  Flossie and I dripped and slogged and proceeded west.  At each intersection she shook to rid herself of the rain.  I did some mental shaking, to rid myself of the angst. 
	</p>
	<p>The bus driver had been a good guy.  I&rsquo;d scared him silly, and he&rsquo;d left his post to help.  His duty was to his passengers, but he reacted to what seemed like a higher responsibility&mdash;rescuing the bewildered blind.
	</p>
	<p>Some friends with disabilities claim that &ldquo;people are hell.&rdquo;  They disable us more than our impairments do.  That hasn&rsquo;t been my experience.  At a disability task force meeting recently, a woman asked what we needed. 
	</p>
	<p>&ldquo;More audible traffic signals,&rdquo; another blind person answered.
	</p>
	<p>Others said more education of the public to dispel myths, fears.  
	</p>
	<p>Sure, the bus driver shouldn&rsquo;t push, but let me take his arm.  But who cares about proper technique in a crisis? He didn&rsquo;t have time.  And I&rsquo;ll take rescue over method any day.
	</p>
	<p>Flossie and I met up with Bob, and I told him of the bus driver&rsquo;s kindness.  Here&rsquo;s to an unnamed man behind the wheel of a 61 A, B, or C.  Many, many thanks.  
    </p>
