  <h2>Kid Readers: Super Critics</h2>
  
<p>by Sally Hobart Alexander</p>

	<p>Many children&rsquo;s authors recommend that writers read their work aloud before sending it to a publisher.  This is good advice.
	</p>
	<p>As a blind person I do most of my reading with my ears.  My computer has speech software that reads anything that appears on the monitor.  However, because the synthesized speech has as much musical quality as a robot, I ask my office help or writing group members to read passages of my work aloud to me.  I like to hear it from someone unfamiliar with it.  Then I mark the sour notes and rewrite. 
	</p>
	<p>So in addition to reading aloud to oneself, I recommend that even sighted writers ask another person to read their work aloud.  It&rsquo;s well known that&mdash; unintentionally&mdash;while reading our own material, we cover flaws. In the two children&rsquo;s writing groups that I lead, we make an exchange like this occasionally.  Everyone benefits.
	</p>
	<p>But there&rsquo;s even a better technique that has worked well for me--employing young people, the age of my audience, to read aloud to me.
	</p>
	<p>Notice I say &ldquo;read&rdquo; to me, not listen to me read.  Many members of my writing groups occasionally bring a child to our session.  Members relish these young visitors because they provide age&ndash;appropriate ears and wisdom.  
	</p>
	<p>In my experience, however, the young people don&rsquo;t provide the anticipated help as listeners.  They&rsquo;re shy about offering suggestions to their parents&rsquo; friends.  They&rsquo;re flattered and also inexperienced in offering critical feedback.  At best, the author herself might be able to hear defects in the manuscript while presenting to the youngster.  
	</p>
	<p>So these three approaches to evaluating manuscripts,
</p>
<ul>
<li> reading it aloud to yourself, </li>
<li> having your peer read it aloud,</li>
<li> reading your work to a young person,</li>
</ul>
    <p>can be useful.  But I find that using kids as the readers is the most effective technique of all.
</p>
	<p>At the beginning of my career, I employed my own children.  &ldquo;Nobody says that anymore,&rdquo; they&rsquo;d announce.  &ldquo;Oh, Mom, that&rsquo;s so lame!&rdquo;
</p>
    <p>They were even more candid when they detected schmaltz, as I learned in my sequel to the book I published about going blind.  The memoir ends with my honeymoon on the Irish Dingle Peninsula.  I&rsquo;d found a starfish missing an arm and tossed it back in the ocean.  The last line went something like, &ldquo;I wanted it to grow whole again, as I had.&rdquo;
</p>
	<p>That passage caused an ecstasy of laughter in my daughter, who then read it to her brother and Dad, who tripled the ridicule.  Later, my daughter presented me with a starfish sculpture, again with great hilarity.  Needless to say, I rewrote that passage. 
</p>
    <p>When my kids grew up, I found readers from the neighborhood.  Hired helpers weren&rsquo;t as heartless as my children, and they provided ample service. 
</p>
	<p>First I must have a polished draft, many times revised, before I bring in the young employee.  Then, I record him on tape as a back-up, in case I miss any faults in the manuscript.  I hear where he stumbles.  I hear which words he mispronounces.  Believe it or not, I detect when he begins to read words without interest or without comprehending.  His tone of voice changes.  And, of course, there are the tell-tale yawns!
</p>
    <p>When my ten-year-old reader met the word, &ldquo;phrenology,&rdquo; in a late draft of my biography of Laura Bridgman, I interrupted to say, &ldquo;See if the definition that follows makes the word clear.&rdquo;  Because it didn&rsquo;t, I rewrote.
</p>
	<p>Before a bright eleven-year-old began to read the biography I&rsquo;ve just finished, set during the Civil War, I asked, &ldquo;Do you know about this conflict?&rdquo; 
</p>
	<p>&ldquo;Yes,&rdquo; she said.  &ldquo;Some states broke away from the country because they were angry about paying taxes to King George.&rdquo;
</p>
	<p>Right away, I knew to double-check how specifically I&rsquo;d explained that historical event.
</p>
	<p>As my relationships with these kid critics grow, they are more forthcoming about asking questions or offering advice.  And even if the reading goes smoothly and the reader seems fully engrossed, I ask, &ldquo;Anything you didn&rsquo;t understand?  Any comments?&rdquo;
</p>
	<p>I pay my readers, partly because they have so many activities that beat sitting with an author and a manuscript that needs work.  They are providing an invaluable service, and their impact on my writing is priceless.
</p>
